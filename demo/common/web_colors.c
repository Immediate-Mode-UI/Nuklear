static struct nk_map_name_color web_colors_map;
static void
web_colors_init(struct nk_context *ctx)
{
    unsigned i;

    const char *(names[NK_LEN(web_colors_name_color_rgb)]);
    struct nk_color (colors[NK_LEN(web_colors_name_color_rgb)]);

    for (i = 0; i < NK_LEN(web_colors_name_color_rgb); ++i) {
        names[i] = web_colors_name_color_rgb[i].name;
        colors[i] = nk_rgb_hex(web_colors_name_color_rgb[i].color_rgb);
    }

    nk_map_name_color_init_colors(&web_colors_map, &ctx->memory.pool, names, colors, NK_LEN(web_colors_name_color_rgb));
}

static nk_bool
web_colors_add(struct nk_context *ctx)
{
    return nk_draw_push_map_name_color(ctx, &web_colors_map);
}

static void
web_colors_preview(struct nk_context *ctx)
{
    static nk_bool show_web_colors = nk_false;
    static nk_bool wrap_text = nk_true;
    unsigned i;
    struct nk_color color;

    if (nk_begin(ctx, "Web Colors Preview", nk_rect(50, 50, 360, 320),
        NK_WINDOW_BORDER|NK_WINDOW_MOVABLE|NK_WINDOW_SCALABLE|
        NK_WINDOW_MINIMIZABLE|NK_WINDOW_TITLE))
    {
        static char field_buffer[256] = "Web color red = [color=\"red\"]red[/color]; web color blue = [color=#0000ff]0000ff[/color].";
        static nk_bool field_init = nk_false;
        static int field_len = 0;
        char text_count[32];

        if (!field_init)
        {
            field_init = nk_true;
            field_len = nk_strlen(field_buffer);
        }

        nk_layout_row_static(ctx, 30, 160, 1);
        if (nk_button_label(ctx, "Show Web Colors"))
            show_web_colors = !show_web_colors;

        nk_layout_row_dynamic(ctx, 30, 2);
        nk_checkbox_label(ctx, "Wrap Text", &wrap_text);
        snprintf(text_count, NK_LEN(text_count), "%d/%d", field_len, (int)NK_LEN(field_buffer) - 1);
        nk_label(ctx, text_count, NK_TEXT_RIGHT);

        nk_layout_row_dynamic(ctx, 30, 1);
        nk_edit_string(ctx, NK_EDIT_SIMPLE, field_buffer, &field_len, NK_LEN(field_buffer), nk_filter_default);

        nk_layout_row_dynamic(ctx, 150, 1);
        nk_draw_push_color_inline(ctx, NK_COLOR_INLINE_TAG);
        if (wrap_text)
            nk_text_wrap(ctx, field_buffer, field_len);
        else
            nk_text(ctx, field_buffer, field_len, NK_TEXT_ALIGN_TOP|NK_TEXT_ALIGN_LEFT);
        nk_draw_pop_color_inline(ctx);
    }
    nk_end(ctx);

    if (show_web_colors)
    {
        if (nk_begin(ctx, "Web Colors", nk_rect(50, 50, 360, 320),
            NK_WINDOW_BORDER|NK_WINDOW_MOVABLE|NK_WINDOW_SCALABLE|
            NK_WINDOW_MINIMIZABLE|NK_WINDOW_TITLE))
        {
            for (i = 0; i < NK_LEN(web_colors_name_color_rgb); ++i) {
                nk_layout_row_dynamic(ctx, 20, 2);
                nk_label(ctx, web_colors_name_color_rgb[i].name, NK_TEXT_LEFT);
                color = nk_rgb_hex(web_colors_name_color_rgb[i].color_rgb);
                nk_label_colored(ctx, web_colors_name_color_rgb[i].color_rgb, NK_TEXT_LEFT, color);
            }
        }
        nk_end(ctx);
    }
}

